// Sebagai reminder, Java termasuk case-sensitive languange, jadi dia sensitif terhadap besar kecilnya awalan suatu kalimat. 

public class index {
  public static void main (String[] args) {
    int myNum = 23; // tipe data int
    char myChar = 'A'; // tipe data char
    float myFloatNum = 5.99f; // Tipe data Float
    boolean myBool = true; // Tipe data boolean
    String myText = "Halo semua, dan"; // Tipe data String, ditulis dengan huruf awalan besar. 

    System.out.println(myNum); 
    System.out.println(myChar); 
    System.out.println(myFloatNum); 
    System.out.println(myBool); 
    System.out.println(myText);

    System.out.println("Halo Java!");
    System.out.println("Hai, Saya Haikel. "); 
  }
}
